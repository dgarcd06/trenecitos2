
public class Colision {
		private int pos_y;
		private int pos_x;
		public Colision(int posx,int posy) {
			this.pos_x = posx;
			this.pos_y = posy;
		}
		/**
		 * @return the pos_y
		 */
		public int getPos_y() {
			return pos_y;
		}
		/**
		 * @return the pos_x
		 */
		public int getPos_x() {
			return pos_x;
		}
}
